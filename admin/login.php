<?php
session_start();
require_once '../config/db_connect.php';

$error = '';
$success = '';

// If already logged in, redirect to dashboard
if (isset($_SESSION['admin_logged_in']) && $_SESSION['admin_logged_in'] === true) {
    header("Location: ../index.php");
    exit();
}

// Handle login form submission
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $username = sanitize_input($_POST['username']);
    $password = $_POST['password'];
    
    if (empty($username) || empty($password)) {
        $error = "Please enter both username and password.";
    } else {
        // Check credentials (using MD5 for demo - use password_hash in production)
        $query = "SELECT * FROM admin WHERE username = '$username' AND password = MD5('$password')";
        $result = fetch_single($query);
        
        if ($result) {
            // Login successful
            $_SESSION['admin_logged_in'] = true;
            $_SESSION['admin_id'] = $result['admin_id'];
            $_SESSION['admin_username'] = $result['username'];
            $_SESSION['admin_name'] = $result['full_name'];
            
            // Update last login
            execute_query("UPDATE admin SET last_login = NOW() WHERE admin_id = " . $result['admin_id']);
            
            header("Location: ../index.php");
            exit();
        } else {
            $error = "Invalid username or password.";
        }
    }
}
